    public function store(Request $request) {
        $request->validate([
            'content'=>'required',
        ]);

        $new_comments = new Comment();
        $new_comments->user_id = Auth::id();
        $new_comments->post_id = $request->post_id;
        $new_comments->content = $request->content;
        $new_comments->save();
        return back();
    }

